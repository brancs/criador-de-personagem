export default class Character{
    constructor(name, gender, origin, rpgClass, rpgClassN, region, skillSet, status, runicPulse, runicPulseN){
        this.name = name;
        this.gender = gender;
        this.origin = origin;
        this.rpgClass = rpgClass;
        this.rpgClassN = rpgClassN;
        this.region = region;
        this.skillSet = skillSet;
        this.status = status;
        this.runicPulse = runicPulse;
        this.runicPulseN = runicPulseN;
    }
}